// Текущий язык (по умолчанию русский)
// Проверяем, что сохраненный язык валидный, иначе используем 'ru'
const savedLang = localStorage.getItem('language');
let currentLanguage = (savedLang === 'ru' || savedLang === 'en' || savedLang === 'tr') ? savedLang : 'ru';
if (!savedLang || (savedLang !== 'ru' && savedLang !== 'en' && savedLang !== 'tr')) {
  localStorage.setItem('language', 'ru');
}

/**
 * Получить перевод по ключу
 * @param {string} key - Ключ перевода
 * @param {Object} params - Параметры для подстановки
 * @returns {string} - Переведенный текст
 */
export const t = (key, params = {}) => {
  const translation = translations[currentLanguage]?.[key] || translations.ru[key] || key;
  
  // Подстановка параметров
  if (params && Object.keys(params).length > 0) {
    return translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey] !== undefined ? params[paramKey] : match;
    });
  }
  
  return translation;
};

/**
 * Установить язык
 * @param {string} lang - Код языка ('ru', 'en' или 'tr')
 */
export const setLanguage = (lang) => {
  if (translations[lang]) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    updateUI();
  }
};

/**
 * Получить текущий язык
 * @returns {string} - Код языка
 */
export const getLanguage = () => currentLanguage;

/**
 * Обновить весь интерфейс
 */
export const updateUI = () => {
  // Обновляем все элементы с data-i18n атрибутом
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const text = t(key);
    
    // Если элемент - input placeholder или textarea placeholder
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      if (element.hasAttribute('placeholder')) {
        element.setAttribute('placeholder', text);
      } else {
        element.value = text;
      }
    } else if (element.tagName === 'OPTION') {
      // Для option обновляем текст
      element.textContent = text;
    } else if (element.tagName === 'LABEL' && element.hasAttribute('for')) {
      // Для label просто обновляем текст
      element.textContent = text;
    } else if (element.tagName === 'STRONG' || element.tagName === 'B') {
      // Для strong/b обновляем текст, сохраняя тег
      element.textContent = text;
    } else if (element.tagName === 'SPAN' && element.parentElement && 
               (element.parentElement.tagName === 'BUTTON' || element.parentElement.classList.contains('btn'))) {
      // Для span внутри кнопок обновляем текст
      element.textContent = text;
    } else {
      // Для остальных элементов обновляем textContent
      // Но только если внутри нет других элементов с data-i18n
      const hasNestedI18n = element.querySelector('[data-i18n]');
      if (!hasNestedI18n) {
        element.textContent = text;
      } else {
        // Если есть вложенные элементы, обновляем только сам элемент
        const children = Array.from(element.childNodes);
        const hasOnlyText = children.every(node => 
          node.nodeType === Node.TEXT_NODE || 
          (node.nodeType === Node.ELEMENT_NODE && (node.hasAttribute('data-i18n') || node.tagName === 'SPAN' || node.tagName === 'STRONG' || node.tagName === 'B'))
        );
        if (hasOnlyText) {
          // Сохраняем структуру, обновляем только текстовые узлы
          const textNodes = children.filter(node => node.nodeType === Node.TEXT_NODE);
          if (textNodes.length > 0) {
            // Если есть только один текстовый узел и он первый, заменяем его
            const firstTextNode = textNodes[0];
            if (firstTextNode === children[0] && textNodes.length === 1) {
              firstTextNode.textContent = text;
            } else {
              element.textContent = text;
            }
          }
        }
      }
    }
  });
  
  // Обновляем title атрибуты
  document.querySelectorAll('[data-i18n-title]').forEach(element => {
    const key = element.getAttribute('data-i18n-title');
    element.setAttribute('title', t(key));
  });
  
  // Обновляем placeholder атрибуты
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    element.setAttribute('placeholder', t(key));
  });
  
  // Обновляем aria-label атрибуты
  document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
    const key = element.getAttribute('data-i18n-aria-label');
    element.setAttribute('aria-label', t(key));
  });
  
  // Вызываем событие изменения языка для других модулей
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: currentLanguage } }));
};

// Инициализация при загрузке
if (typeof window !== 'undefined') {
  // Устанавливаем язык из localStorage или по умолчанию русский
  const savedLang = localStorage.getItem('language');
  if (savedLang === 'ru' || savedLang === 'en' || savedLang === 'tr') {
    currentLanguage = savedLang;
  } else {
    // По умолчанию русский язык
    currentLanguage = 'ru';
    localStorage.setItem('language', 'ru');
  }
  
  // Обновляем UI после загрузки DOM
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
      updateUI();
    });
  } else {
    // Если DOM уже загружен, обновляем сразу
    updateUI();
  }
}
